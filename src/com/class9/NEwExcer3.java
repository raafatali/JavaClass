package com.class9;

public class NEwExcer3 {
	

	public static void main(String [] args) {
	
	for ( int i=0; i<=6; i++ ) {
		 
        for(int a=0; a<=i; a++) {
            
            System.out.print("*");
        }
        System.out.println(); 
	
	}
	
	for ( int i=6; i>0; i-- ) {
		 
        for(int a=6; a>i; a--) {
            
            System.out.print("+");
        }
        System.out.println();
	
	}
} 
}