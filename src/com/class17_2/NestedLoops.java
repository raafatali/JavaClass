package com.class17_2;

public class NestedLoops {
	
	public static void main(String [] args) {
		
		
		NestedLoops name=new NestedLoops();
		name.printNumber();
		
	}
	void printNumber() {
		
		for (int i=1; i<=9; i++) {
		
		    for (int y=1; y<=5; y++) {
		    	
		    	System.out.print(i);
		    }
		
		System.out.println();
	
  
		 
        }
    }
}
